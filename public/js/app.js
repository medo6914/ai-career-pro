// ─── URL params ───
const params = new URLSearchParams(window.location.search);
if (params.get('success') === 'true') {
  const tier = params.get('tier') || 'pro';
  const sessionId = params.get('session_id');
  if (sessionId) {
    fetch(`/api/verify-session?session_id=${sessionId}&clientId=${clientId}`)
      .then(r => r.json())
      .then(() => { alert(`✅ تم الترقية إلى ${tier === 'vip' ? 'VIP' : 'Pro'}`); window.history.replaceState({}, '', '/'); location.reload(); })
      .catch(() => { alert(`✅ تم الترقية`); window.history.replaceState({}, '', '/'); location.reload(); });
  } else {
    alert(`✅ تم الترقية`); window.history.replaceState({}, '', '/'); location.reload();
  }
}
if (params.get('canceled') === 'true') {
  alert('❌ تم إلغاء الدفع'); window.history.replaceState({}, '', '/');
}

