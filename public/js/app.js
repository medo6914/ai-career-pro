async function handlePayment(tier, method) {
  if (method === 'stripe') await handleStripe(tier);
  else if (method === 'paypal') await handlePayPal(tier);
  else if (method === 'vodafone_cash' || method === 'etisalat_cash') await handleWallet(tier, method);
}

async function handleStripe(tier) {
  try {
    const res = await fetch('/api/create-checkout', {
      method: 'POST', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ priceId: tier === 'vip' ? 'price_vip' : 'price_pro', clientId })
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error);
    if (data.url) window.location.href = data.url;
    else throw new Error('No URL');
  } catch (err) {
    alert(err.message || 'Stripe error. Check .env settings.');
  }
}

async function handlePayPal(tier) {
  try {
    const orderRes = await fetch('/api/create-paypal-order', {
      method: 'POST', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ tier, clientId })
    });
    const order = await orderRes.json();
    if (!orderRes.ok) throw new Error(order.error);
    if (order.id) {
      const url = order.approvalUrl || `https://www.sandbox.paypal.com/checkoutnow?token=${order.id}`;
      window.location.href = url;
    }
  } catch (err) {
    alert(err.message || 'PayPal error.');
  }
}

async function handleWallet(tier, method) {
  const phone = prompt(i18n[currentLang]['enter_phone']);
  if (!phone || phone.length < 10) {
    alert(i18n[currentLang]['phone_required']);
    return;
  }

  try {
    const res = await fetch('/api/create-paymob-intent', {
      method: 'POST', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ tier, clientId, paymentMethod: method, phone })
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error);

    if (data.redirect && data.url) {
      window.location.href = data.url;
    }
  } catch (err) {
    alert(i18n[currentLang]['wallet_error']);
  }
}

// ─── Form submit ───
document.getElementById('aiForm').addEventListener('submit', async (e) => {
  e.preventDefault();
  const name = document.getElementById('name').value.trim();
  if (!name) return alert(currentLang === 'ar' ? 'أدخل الاسم' : 'Enter your name');

  document.getElementById('generateBtn').style.display = 'none';
  document.getElementById('loadingIndicator').style.display = 'block';

  try {
    const res = await fetch('/api/generate', {
      method: 'POST', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        tool: currentTool, language: currentLang, name,
        jobTitle: document.getElementById('jobTitle').value.trim(),
        company: document.getElementById('company').value.trim(),
        skills: document.getElementById('skills').value.trim(),
        experience: document.getElementById('experience').value.trim(),
        details: document.getElementById('details').value.trim(),
        clientId, aiProvider: currentProvider
      })
    });
    const data = await res.json();
    if (res.status === 402) { alert(data.message || i18n[currentLang]['error_limit']); document.getElementById('pricing').scrollIntoView({ behavior: 'smooth' }); return; }
    if (!res.ok) { alert(data.details || i18n[currentLang]['error_generic']); return; }
    document.getElementById('resultContent').textContent = data.result;
    document.getElementById('resultSection').style.display = 'block';
    document.getElementById('formSection').style.display = 'none';
    window.scrollTo({ top: document.getElementById('resultSection').offsetTop - 80, behavior: 'smooth' });
  } catch (err) {
    alert(i18n[currentLang]['error_api']);
  } finally {
    document.getElementById('generateBtn').style.display = '';
    document.getElementById('loadingIndicator').style.display = 'none';
  }
});

