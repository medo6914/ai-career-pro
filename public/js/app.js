// ─── Check usage ───
async function checkUsage() {
  try {
    const res = await fetch('/api/usage', {
      method: 'POST', headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ clientId })
    });
    const data = await res.json();
    const remaining = data.tier === 'free' ? data.remaining : '∞';
    const limit = data.tier === 'free' ? data.limit : '∞';
    document.getElementById('usageCount').textContent = `${remaining} / ${limit}`;
    document.getElementById('usageBar').style.width = `${data.tier === 'free' ? (data.remaining / data.limit) * 100 : 100}%`;
    const badge = document.getElementById('tierBadge');
    badge.className = `tier-badge ${data.tier}`;
    badge.textContent = i18n[currentLang][`tier_${data.tier}`] || data.tier;

    const aiSection = document.getElementById('aiProviderSection');
    if (data.tier === 'vip') {
      aiSection.style.display = 'block';
      currentProvider = data.aiProvider || 'openrouter';
      document.querySelectorAll('.ai-provider-opt').forEach(opt => {
        opt.disabled = false;
        opt.classList.toggle('active', opt.dataset.provider === currentProvider);
      });
    } else {
      aiSection.style.display = 'none';
    }
  } catch (e) { console.log('Server not running'); }
}

